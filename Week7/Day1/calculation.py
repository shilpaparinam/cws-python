"""
Make 4 functions

addition, subtraction, multiply, division

Each functions take 2 parameter and return the respective
"""


def addition(n1, n2):
    return n1 + n2


def subtraction(a1, a2):
    return a1 - a2


def multiply(e1, e2):
    return e1 * e2


def division(f1, f2):
    return f1 / f2


x = addition(10, 20)
y = subtraction(20, 30)
z = multiply(30, 40)
w = division(20, 4)
print(x)
print(y)
print(z)
print(w)
